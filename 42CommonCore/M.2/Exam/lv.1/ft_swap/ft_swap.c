#include <stdio.h>
#include <unistd.h>

void	ft_swap(int *a, int *b)
{
	int	x;

	x = *a;
	*a = *b;
	*b = x;
}

int	main(void)
{
	int	a;
	int	b;

	a = 7;
	b = 12;
	ft_swap(&a, &b);
	printf("a:%d, b:%d", a, b);
	return (0);
}
